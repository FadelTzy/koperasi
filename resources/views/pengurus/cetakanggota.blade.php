        function staffupd(id) {
            $('#exampleLargeModalu').modal('show');
            $('#editdata').trigger("reset");
            $("#idu").val(id.id);
            $("#namau").val(id.name);
            $("#emailu").val(id.email);
            if (id.detail != null) {
                $("#pekerjaanu").val(id.detail.pekerjaan ?? '-');

                $("#nou").val(id.detail.nohp ?? '-');
                $("#alamatu").val(id.detail.alamat ?? '-');
            }
            $("#kodeu").val(id.kode);
            var $radios = $('input:radio[name=jku]');
            if ($radios.is(':checked') === false) {
                $radios.filter('[value=' + id.jk + ']').prop('checked', true);
            }
            $("#usernameu").val(id.username);
